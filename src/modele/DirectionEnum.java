package modele;

/**
 * L'énumération DirectionEnum représente les différentes directions possibles.
 * Elle est utilisée pour spécifier les déplacements dans une grille de jeu.
 */
public enum DirectionEnum {
    
    /**
     * La direction vers le haut.
     */
    HAUT,
    
    /**
     * La direction vers le bas.
     */
    BAS,
    
    /**
     * La direction vers la gauche.
     */
    GAUCHE,
    
    /**
     * La direction vers la droite.
     */
    DROITE
    
}
